public class Logical {
    public static void main(String[] args) {
        System.out.println("\n\t Logical AND && ");
        System.out.println((2>3)&&(4<1));
        System.out.println("\n\t Logical OR ||  ");
        System.out.println((2<3)||(4<1));
         System.out.println("\n\t Logical NOT !  ");
         System.out.println(!(3>2));
    }
    
}

// output:

//          Logical AND &&
// false

//          Logical OR ||
// true

//          Logical NOT !
// false


