public class PrimitiveDataTypes{
     public static void main(String[] args) {
            System.out.println("\n\t Primitive Data Types \n");
            byte b=8;
            System.out.println("\n\t"+b);
            char ch='a';
            System.out.println("\n\t"+ch);
            boolean var = false;
            float price = 10.5f;
            int number = 25;
            short n = 240;
            long l=1023456789;
            double decimal = 3.145678;
            System.out.println("\n\t"+var);
            System.out.println("\n\t"+price);
            System.out.println("\n\t"+number);
            System.out.println("\n\t"+n);
            System.out.println("\n\t"+decimal);
            System.out.println("\n\t"+l);


    }
}


