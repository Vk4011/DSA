
import java.util.*;

public class Sum {
public static void main(String[] args) {
    Scanner input = new Scanner(System.in);
    System.out.println("\n\t Sum of Two Nums");
    int a=input.nextInt();
    System.out.println("\n\t Enter a value :"+a);
    int b=input.nextInt();
    System.out.println("\n\t Enter b value :"+b);
    int sum = a+b;
    System.out.println("\n\t Sum of two nums : "+sum);

}
    
}
