public class Ifelse{
    public static void main(String[] args) {
        int a=4,b=9;
        if(a>b){
            System.out.println("\n\t Largest value : "+a);
        }
        else{
            System.out.println("\n\t Largest value : "+b);
        }
    }
}

// output:
//  Largest value : 9

 

