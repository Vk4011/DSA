public class Star{
    public static void main(String[] args) {
        int n=5;
        System.out.println("\n\t Star Pattern \n");
        for(int line=1;line<=n;line++){
            //one line
            for(int star=1;star<=line;star++){
                System.out.print("\t*");
            }
        
        System.out.println("\n");
        }
        
    }
}

// output:
//     Star Pattern

//         *

//         *       *

//         *       *       *

//         *       *       *       *

//         *       *       *       *       *





