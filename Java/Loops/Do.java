public class Do {
    public static void main(String[] args) {
        int counter=1;
        do{
            System.out.println("\n\t Hello world !");
            counter++;
        }while(counter<=5);
        System.out.println("\n\t printed Hello world 5 time ");
        System.out.println("\n\t Note : In do while() Statement terminatior is used in while(); ");
    }
    
}

// output:
// Hello world !

//          Hello world !

//          Hello world !

//          Hello world !

//          Hello world !

//          printed Hello world 5 time

//          Note : In do while() Statement terminatior is used in while();


