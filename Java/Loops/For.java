public class For {
    public static void main(String[] args) {
        System.out.println("\n\t Square Pattern");
        for(int i=1;i<=4;i++){
            System.out.println("\n\t*  *  *  *");
        }
        System.out.println("\n\t printed Square ... !");

        int line=1;
        while(line<=3){
            System.out.println("\n\t *  *  *");
            line++;
        }
    

    }
}


