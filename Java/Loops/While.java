public class While{
    public static void main(String[] args) {
        int count=0;
        while(count<6){
            System.out.println("\n\t Hello");
            count++;// loop will stop after reaching count 
            }
        System.out.println("\n\t Hello  6 times..");
    }
}
// output:
//     Hello

//          Hello

//          Hello

//          Hello

//          Hello

//          Hello

//          Hello  6 times..